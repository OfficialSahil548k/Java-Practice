package Basics;
public class operators {
    public static void main(String[] args) {
        int a = 5;
        int b = 25;
        System.out.println("add = " + (a+b));
        System.out.println("subtract = " + (b-a));
        System.out.println("multiply = " + (a*b));
        System.out.println("divide = " + (b/a));
    }
}
